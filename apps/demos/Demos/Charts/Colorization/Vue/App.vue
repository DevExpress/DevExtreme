<template>
  <div>
    <DxTreeMap
      id="treemap"
      :data-source="salesAmount"
      :colorizer="typeOptions"
      value-field="salesAmount"
      title="Sales Amount by Product"
    >
      <DxTooltip
        :enabled="true"
        :customize-tooltip="customizeTooltip"
        format="currency"
      />
    </DxTreeMap>
    <div class="options">
      <div class="caption">Options</div>
      <div class="option">
        <span>Colorization Type</span>
        <DxSelectBox
          :data-source="colorizationOptions"
          :width="200"
          :input-attr="{ 'aria-label': 'Colorization Type' }"
          v-model:value="selectedType"
          display-expr="name"
          value-expr="name"
        />
      </div>
    </div>
  </div>
</template>
<script setup lang="ts">
import { computed, ref } from 'vue';
import DxTreeMap,
{
  DxTooltip,
} from 'devextreme-vue/tree-map';
import { DxSelectBox } from 'devextreme-vue/select-box';
import { salesAmount, colorizationOptions } from './data.ts';

const selectedType = ref(colorizationOptions[2].name);
const typeOptions = computed(
  () => colorizationOptions.find(({ name }) => name === selectedType.value).options,
);
const customizeTooltip = ({ node, node: { data: { name } }, valueText }) => (
  {
    text: node.isLeaf()
      ? `<span class='product'>${name}</span><br/>Sales Amount: ${valueText}`
      : null,
  }
);
</script>
