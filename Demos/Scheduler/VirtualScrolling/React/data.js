export function generateAppointments() {
  const data = [];
  const resourcesAmount = resources.length;
  let increment = 1;

  for(let resourceId = 0; resourceId < resourcesAmount; resourceId += increment) {

    if(++increment === 3) {
      increment = 1;
    }

    dataSet.forEach(function(item) {
      const humanId = (item.humanId + resourceId + item.humanId) % resourcesAmount;
      data.push({
        text: item.text,
        startDate: item.startDate,
        endDate: item.endDate,
        humanId
      });
    });
  }

  return data;
}
